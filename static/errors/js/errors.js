// ========================================
// ERROR 403 (errors_403.html)
// ========================================

// 로그인 페이지로 이동
function goToLogin() {
    window.location.href = '/accounts/login/';
}

// ========================================
// ERROR 500 (errors_500.html)
// ========================================

// 페이지 다시 시도 함수
function retryPage() {
    // 잠시 대기 후 페이지 새로고침 (서버 오류가 일시적일 수 있음)
    setTimeout(function() {
        location.reload();
    }, 2000);
}

// ========================================
// 공통 함수들
// ========================================

// 홈으로 돌아가기
function goToHome() {
    window.location.href = '/';
}

// 뒤로가기
function goBack() {
    history.back();
}
